bool is_valid(char a)
{
	if (a == '0' || a == '1' || a == 'N' || a == 'S' || a == 'E' || a == 'W' || a == ' ')
		return(true);
	return (false);
}

int space_skip(char *str)
{
	int i = 0;
	
	while (str[i] && ((str[i] >= 9 && str[i] <= 13) || str[i] == 32))
		i++;
	return(i);
}

